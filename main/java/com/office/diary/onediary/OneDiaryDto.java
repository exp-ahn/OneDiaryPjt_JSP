package com.office.diary.onediary;

public class OneDiaryDto {

	private int d_no;
	private String d_word;
	private String m_id;
	private String d_reg_date;
	private String d_mod_date;
	
	public int getD_no() {
		return d_no;
	}
	public void setD_no(int d_no) {
		this.d_no = d_no;
	}
	public String getD_word() {
		return d_word;
	}
	public void setD_word(String d_word) {
		this.d_word = d_word;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public String getD_reg_date() {
		return d_reg_date;
	}
	public void setD_reg_date(String d_reg_date) {
		this.d_reg_date = d_reg_date;
	}
	public String getD_mod_date() {
		return d_mod_date;
	}
	public void setD_mod_date(String d_mod_date) {
		this.d_mod_date = d_mod_date;
	}
	
}
